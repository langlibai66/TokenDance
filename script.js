function computeAxisScores(specs) {
  const cleaning =
    3.9 +
    normalize(specs.movementsPerMin, 40000, 84000) * 3 +
    normalize(specs.oscillationAngle, 0, 60) * 0.9 +
    normalize(specs.plaqueBoost, 40, 100) * 1.2 +
    (specs.roundHead ? 0.7 : 0) +
    normalize(specs.modeCount, 1, 4) * 0.4;

  const battery =
    4 +
    normalize(specs.batteryDays, 14, 35) * 5 +
    (specs.fastCharge ? 0.5 : 0) +
    (specs.magneticCharge ? 0.2 : 0);

  const gum =
    4.1 +
    (specs.pressureSensor ? 1.7 : 0) +
    (specs.softBristles ? 0.8 : 0) +
    normalize(specs.gumModes, 1, 3) * 1 +
    normalize(specs.appLevels, 1, 32) * 0.9 +
    (specs.roundHead ? 0.5 : 0);

  const noise = 3.8 + inverseNormalize(specs.noiseDb, 42, 62) * 5.4 + (specs.quietTech ? 0.6 : 0);

  const design =
    3.7 +
    normalize(specs.designValue, 7.5, 9.5) * 4.8 +
    (specs.screen ? 0.8 : 0) +
    (specs.premiumMaterial ? 0.5 : 0) +
    (specs.chargingDock ? 0.3 : 0);

  const price =
    3.8 +
    inverseNormalize(specs.priceCny, 599, 899) * 5.4 +
    normalize(specs.brushHeads, 1, 3) * 0.6;

  return [
    { label: "清洁力", value: roundToOne(clamp(cleaning, 0, 10)) },
    { label: "续航", value: roundToOne(clamp(battery, 0, 10)) },
    { label: "护龈", value: roundToOne(clamp(gum, 0, 10)) },
    { label: "噪音", value: roundToOne(clamp(noise, 0, 10)) },
    { label: "颜值", value: roundToOne(clamp(design, 0, 10)) },
    { label: "价格", value: roundToOne(clamp(price, 0, 10)) }
  ];
}

