function buildRadarPoints(values, progress = 1) {
  const centerX = 90;
  const centerY = 90;
  const maxRadius = 64;
  const total = values.length;

  return values.map((value, index) => {
    const angle = -Math.PI / 2 + (Math.PI * 2 * index) / total;
    const radius = maxRadius * (value / 10) * progress;
    const x = centerX + Math.cos(angle) * radius;
    const y = centerY + Math.sin(angle) * radius;
    return { x, y };
  });
}

function renderRadar(values, progress = 1) {
  const points = buildRadarPoints(values, progress);
  elements.radarArea.setAttribute(
    "points",
    points.map((point) => `${point.x.toFixed(1)},${point.y.toFixed(1)}`).join(" ")
  );

  elements.radarPoints.innerHTML = points
    .map(
      (point) => `<circle cx="${point.x.toFixed(1)}" cy="${point.y.toFixed(1)}" r="3.8"></circle>`
    )
    .join("");
}

function animateRadar(values) {
  if (radarAnimationFrame) {
    window.cancelAnimationFrame(radarAnimationFrame);
  }

  const startTime = window.performance.now();
  const duration = 640;

  function step(now) {
    const elapsed = now - startTime;
    const progress = Math.min(elapsed / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    renderRadar(values, eased);

    if (progress < 1) {
      radarAnimationFrame = window.requestAnimationFrame(step);
    }
  }

  radarAnimationFrame = window.requestAnimationFrame(step);
}

